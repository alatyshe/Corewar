#include "../../header/libft.h"

void	ft_strdel(char **as)
{
	size_t	i;

	i = 0;
	if (as)
	{
		while (as[i])
			i++;
		while (as[i])
		{
			free(as[i]);
			i--;
		}
		*as = NULL;
	}
}
