#include "../../header/libft.h"

int				skip_spaces(char *str)
{
	int			i;

	i = 0;
	if (str)
	{
		while (str[i] == '\n' || str[i] == '\t' || str[i] == '\v' ||
			str[i] == '\r' || str[i] == '\f' || str[i] == '\n' ||
			str[i] == ' ')
			i++;
	}
	return (i);
}
